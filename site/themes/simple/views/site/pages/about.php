<h1>An SEO Api for Developers</h1>

<p>A while back a friend asked me to help on some search engine
	optimization for his company's website. In general SEO is very
	simple. Just pick your keywords based on how much competition there
	is and follow some basic principles.</p>

<p>This sounds simple enough, but going through an entire site
	counting words, examining H1, H2, links, etc... is a lot of work.</p>

<p>So I did what any rational person would do, looked for tools on the
	web that would do this for me. I searched high and low and found
	nothing free and nothing that good. And the pay sites for SEO reports
	were really expensive. The free SEO tools were not very good. Hence
	this site...</p>

<p>I wrote the repetitive code to do the simple boring work of
	counting words, examining tags and other basic SEO tasks. I wrapped
	this code into a simple api that you can use to create your own
	customized reports.</p>
	
<h2>Take SEO a Step Further</h2>

<p>So I figure this is not enough to lure developers in and really and the api
    is not that useful on its own.  So on top of an API I spent time documenting
	and adding a javascript library to simplify calling the api.
	</p>
	
<h2>End Goal for SEO Reporting</h2>

<p>I want to have the ability for developers to write their own
	reports using a framework.  In adidtion, I plan to figure out
	a way to open up the source code to integrate and support
	outside support and extension of the api.
<p>

<p>So if you have ideas and comments about what can be added to make this
	a better SEO reporting tool; then please send comments.
	</p>